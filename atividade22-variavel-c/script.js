let valorA = parseInt(prompt("Informe o valor A: "));
let valorB = parseInt(prompt("Informe o valor B: "));

if (valorA == valorB) {
  let variavelC = valorA + valorB;
  console.log("Variável C = " + variavelC);
} else {
  let variavelC = valorA * valorB;
  console.log("Variável C = " + variavelC);
}
